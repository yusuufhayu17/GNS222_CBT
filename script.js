// KaTeX rendering function
  function renderLatex(str) {
    if (!str) return str;

    // Render LaTeX between $...$
    return str.replace(/\$(.*?)\$/g, (match, latex) => {
      try {
        return katex.renderToString(latex, {
          throwOnError: false,
          displayMode: false
        });
      } catch (e) {
        console.error("KaTeX error:", e);
        return match;
      }
    });
  }

